            <h3>Find Information about an Animal</h3>
            <p>Once you are logged in, return to the Welcome Page. The first thing you can try is "Find Information about an Animal". When you click on that link, it will take you to a page with a dropdown menu. Feel free to pull up information about any of the animals shown in the dropdown.</p>
            
            <h3>Look for the Wildlife Spotted in an Area</h3>
            <p>Using this feature, you can search for information about wildlife that other users have spotted in a certain area. There are a number of ways you can enter the location information. You can enter it in City, State format. You can click on the button to use your current location which will enter the latitude and longitude of your current location in the text box (Note to Chrome users: you must be on the secure "https" version of the site to use this feature). You can enter a street address. You can enter a landmark (e.g., "Pikes Peak"). Or you could try other location name formats to see if they work. When you are logged in, you can also choose to save your search for future reference. Right now, the site is still being constructed, so users are not able to enter their own wildlife sightings, but we have entered test data for the following locations:</p>
            <ul class="list-group">
                <li class="list-group-item">Kruger National Park, South Africa</li>
                <li class="list-group-item">Patagonia, Chile</li>
                <li class="list-group-item">Sichuan, China</li>
                <li class="list-group-item">Ohiopyle, PA</li>
            </ul>
            
            <h3>Upload and View Photos of Wildlife and Human Activity</h3>
            <p>This feature allows you to upload photos of wildlife and human activity and to view the photos uploaded by other users. There is a generous limit of 64 MB on the photos you are able to upload.</p>
            
            <h3>View Newsfeed of Saved Area</h3>
            <p>The locations that you have previously saved appear in the dropdown menu in this section. You can choose one of the locations and view the latest updates that have been uploaded for those locations. If you are still logged in to the "tester" account, you can start out by viewing the latest updates for Ohiopyle, PA, USA. You will notice 5 updates on the page for both human activities and wildlife sightings, in reverse chronological order. If you want to see more updates, you can click on "See More Results" which will open the page again with 5 additional results. You can continue to click on the "See More Results" button until all of the results have been displayed. Clicking on "Return to Welcome Page" will take you back to the welcome page</p>
    
            <h3>Enjoy Yourself</h3>
